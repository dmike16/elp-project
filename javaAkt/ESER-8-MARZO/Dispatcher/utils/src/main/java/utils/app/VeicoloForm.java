package utils.app;

public class VeicoloForm {
	private String targa;
	private String modello;
	private String kw;
	private String cilindrata;
	private String velocita;
	private String posti;
	private String cat;
	private String comb;
	private String immatricolazione;
	
	public String getCilindrata() {
		return cilindrata;
	}
	public void setCilindrata(String cilindrata) {
		this.cilindrata = cilindrata;
	}
	public String getVelocita() {
		return velocita;
	}
	public void setVelocita(String velocita) {
		this.velocita = velocita;
	}
	public String getPosti() {
		return posti;
	}
	public void setPosti(String posti) {
		this.posti = posti;
	}
	public String getCat() {
		return cat;
	}
	public void setCat(String cat) {
		this.cat = cat;
	}
	public String getComb() {
		return comb;
	}
	public void setComb(String comb) {
		this.comb = comb;
	}
	
	public VeicoloForm(){}
	public String getTarga() {
		return targa;
	}
	public void setTarga(String targa) {
		this.targa = targa;
	}
	public String getModello() {
		return modello;
	}
	public void setModello(String modello) {
		this.modello = modello;
	}
	public String getKw() {
		return kw;
	}
	public void setKw(String kw) {
		this.kw = kw;
	}
	public String getImmatricolazione() {
		return immatricolazione;
	}
	public void setImmatricolazione(String immatricolazione) {
		this.immatricolazione = immatricolazione;
	}
	
	
}
